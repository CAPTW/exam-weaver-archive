"""Helpers for lightweight rich text format metadata."""

from dataclasses import dataclass, field
import re
from typing import List, Optional


@dataclass
class FormattedText:
    text: str
    spans: List[dict] = field(default_factory=list)
    raw_to_normalized: List[Optional[int]] = field(default_factory=list)


def normalize_latex_text(value: str) -> FormattedText:
    """Normalize pasted/PDF equation notations into LaTeX spans."""
    raw = normalize_private_math_glyphs(value)
    output = []
    spans = []
    raw_to_normalized: List[Optional[int]] = [None] * len(raw)
    i = 0

    def append_plain(ch: str, raw_index: int):
        raw_to_normalized[raw_index] = len(''.join(output))
        output.append(ch)

    def append_equation(raw_start: int, raw_end: int, latex: str, map_inner_start=None, inner_text=None):
        latex = _normalize_latex(latex)
        if not latex:
            for raw_index in range(raw_start, raw_end):
                append_plain(raw[raw_index], raw_index)
            return

        start = len(''.join(output))
        output.append(latex)
        end = start + len(latex)
        spans.append({'start': start, 'end': end, 'latex': latex})

        if map_inner_start is not None and inner_text is not None and inner_text == latex:
            for offset, _ in enumerate(inner_text):
                raw_index = map_inner_start + offset
                if raw_start <= raw_index < raw_end:
                    raw_to_normalized[raw_index] = start + offset

    while i < len(raw):
        if raw.startswith(r'\(', i):
            end = raw.find(r'\)', i + 2)
            if end >= 0:
                inner_start = i + 2
                inner = raw[inner_start:end].strip()
                leading = len(raw[inner_start:end]) - len(raw[inner_start:end].lstrip())
                append_equation(i, end + 2, inner, inner_start + leading, inner)
                i = end + 2
                continue

        if raw[i] == '$':
            end = raw.find('$', i + 1)
            if end > i + 1:
                inner_start = i + 1
                inner = raw[inner_start:end].strip()
                leading = len(raw[inner_start:end]) - len(raw[inner_start:end].lstrip())
                append_equation(i, end + 1, inner, inner_start + leading, inner)
                i = end + 1
                continue

        command_match = re.match(r'\\?(sqrt|overline)\s*\{([^{}]+)\}', raw[i:])
        if command_match:
            command = command_match.group(1)
            inner = _normalize_formula_inner(command_match.group(2))
            append_equation(i, i + command_match.end(), rf'\{command}{{{inner}}}')
            i += command_match.end()
            continue

        root_match = re.match(r'ROOT\s*\(\s*([^)]+?)\s*\)', raw[i:], re.IGNORECASE)
        if root_match:
            inner = _normalize_formula_inner(root_match.group(1))
            append_equation(i, i + root_match.end(), rf'\sqrt{{{inner}}}')
            i += root_match.end()
            continue

        formula_match = re.match(
            r'[A-Za-z]\s*=\s*[A-Za-z0-9\\{}\s√×·ㆍ∙•+\-*/^().,θΘ]+',
            raw[i:]
        )
        if formula_match and _looks_like_inline_formula(formula_match.group(0)):
            latex = _normalize_formula_expression(formula_match.group(0))
            append_equation(i, i + formula_match.end(), latex)
            i += formula_match.end()
            continue

        sqrt_match = re.match(r'√\s*\(\s*([^)]+?)\s*\)', raw[i:])
        if sqrt_match:
            inner = _normalize_formula_inner(sqrt_match.group(1))
            append_equation(i, i + sqrt_match.end(), rf'\sqrt{{{inner}}}')
            i += sqrt_match.end()
            continue

        sqrt_match = re.match(r'√\s*\{\s*([^{}]+?)\s*\}', raw[i:])
        if sqrt_match:
            inner = _normalize_formula_inner(sqrt_match.group(1))
            append_equation(i, i + sqrt_match.end(), rf'\sqrt{{{inner}}}')
            i += sqrt_match.end()
            continue

        sqrt_match = re.match(r'√\s*([0-9])(?=[0-9A-Za-z])', raw[i:])
        if sqrt_match:
            inner = _normalize_formula_inner(sqrt_match.group(1))
            append_equation(i, i + sqrt_match.end(), rf'\sqrt{{{inner}}}')
            i += sqrt_match.end()
            continue

        sqrt_match = re.match(r'√\s*([0-9]+(?:\.[0-9]+)?)', raw[i:])
        if sqrt_match:
            inner = _normalize_formula_inner(sqrt_match.group(1))
            append_equation(i, i + sqrt_match.end(), rf'\sqrt{{{inner}}}')
            i += sqrt_match.end()
            continue

        sqrt_match = re.match(r'√\s*([A-Za-z][A-Za-z0-9_]*)', raw[i:])
        if sqrt_match:
            inner = _normalize_formula_inner(sqrt_match.group(1))
            append_equation(i, i + sqrt_match.end(), rf'\sqrt{{{inner}}}')
            i += sqrt_match.end()
            continue

        append_plain(raw[i], i)
        i += 1

    return FormattedText(''.join(output), spans, raw_to_normalized)


def normalize_private_math_glyphs(value: str) -> str:
    """Replace PDF math-font private-use glyphs with readable Unicode text."""
    text = str(value or '')
    text = re.sub(
        r'\ue05c\ue06d?([\ue000-\uf8ffA-Za-z0-9])',
        lambda match: f"√{_private_math_char(match.group(1))}",
        text,
    )
    return ''.join(_private_math_char(ch) for ch in text)


def repair_extracted_text_artifacts(value: str) -> str:
    """Repair high-confidence PDF/OCR text artifacts without changing meaning."""
    text = normalize_private_math_glyphs(value)
    text = _normalize_extracted_whitespace(text)
    text = repair_common_english_ocr_confusables(text)
    text = repair_common_korean_ocr_confusables(text)
    text = repair_subject_header_artifacts(text)
    text = repair_number_unit_order_artifacts(text)
    text = repair_residual_numeric_order_artifacts(text)
    text = repair_common_korean_spacing_artifacts(text)
    text = repair_parenthetical_note_artifacts(text)
    text = repair_misordered_prompt_artifacts(text)
    text = repair_reversed_choice_fragments(text)
    text = _repair_db_audit_findings(text)
    return text


def repair_common_english_ocr_confusables(text: str) -> str:
    """Repair unambiguous zero/one and capital-I OCR substitutions in prose."""

    raw = str(text or "")
    token_replacements = {
        "lnternational": "International",
        "InternationaI": "International",
        "C011ision": "Collision",
        "C011isions": "Collisions",
        "C0IIision": "Collision",
        "C0IIisions": "Collisions",
        "011e": "one",
        "011t": "out",
        "0f": "of",
        "t0": "to",
        "0i1": "oil",
        "A11": "All",
        "a11": "all",
        "ManuaI": "Manual",
        "RuIe": "Rule",
        "TerritoriaI": "Territorial",
        "CaIIing": "Calling",
        "ldentification": "Identification",
        "sh1P": "ship",
        "sh1Ps": "ships",
        "tO": "to",
        "Of": "of",
        "whO": "who",
        "alSO": "also",
        "ThiS": "This",
        "compr1S1ng": "comprising",
        "mportant": "important",
        "Convent10n": "Convention",
        "t011S": "tons",
    }
    for damaged, repaired in token_replacements.items():
        raw = re.sub(
            rf"(?<![0-9A-Za-z]){re.escape(damaged)}(?![0-9A-Za-z])",
            repaired,
            raw,
        )
    raw = re.sub(
        r"(?<![0-9A-Za-z])f011[^0-9A-Za-z\s]{0,3}Ming(?![0-9A-Za-z])",
        "following",
        raw,
    )
    return raw


def repair_common_korean_ocr_confusables(text: str) -> str:
    """Repair Korean OCR substitutions whose surrounding phrase is unambiguous."""

    raw = str(text or "")
    raw = re.sub(r"(?<![가-힣])(다읔|디음)(?=\s)", "다음", raw)
    raw = re.sub(r"^\s*[c口](?=음\s)", "다", raw)
    raw = re.sub(r"오으\s*것", "옳은 것", raw)
    raw = re.sub(r"(?<![가-힣])云치", "조치", raw)
    raw = re.sub(r"조\s*大\s*1(?=\s*(?:를|가|를 다))", "조치", raw)
    raw = re.sub(r"수리\s*八치", "수리 조치", raw)
    raw = re.sub(
        r"설치(?:以\}|祆卜|必\})용",
        "설치·사용",
        raw,
    )
    raw = re.sub(r"(\d+)(년|개월)口卜다", r"\1\2마다", raw)
    raw = raw.replace("해잉오염", "해양오염")
    return raw


def repair_subject_header_artifacts(text: str) -> str:
    """Remove parsed page subject headers and any following same-field spillover."""
    raw = str(text or '').strip()
    raw = re.sub(
        r'\s*(?:[\[【]\s*제\s*\d+\s*과목\s*[:：]\s*[^\]】\r\n]+[\]】]'
        r'|\(\s*제\s*\d+\s*과목\s*[:：]\s*[^)\r\n]+\)'
        r'|제\s*\d+\s*과목\s*[:：]\s*\S+).*$',
        '',
        raw,
    )
    return raw.strip()


def repair_residual_numeric_order_artifacts(text: str) -> str:
    """Repair high-confidence numeric fragments left inside Korean words."""
    raw = str(text or '').strip()
    raw = _repair_residual_numeric_whole_text(raw)
    raw = _repair_common_residual_numeric_patterns(raw)
    return raw


def repair_common_korean_spacing_artifacts(text: str) -> str:
    """Repair tiny, high-confidence Korean spacing artifacts from PDF joins."""
    raw = str(text or '').strip()
    raw = re.sub(
        r'(?<![A-Za-z])r([가-힣][가-힣\s·ㆍ및]{0,40}법)\]',
        r'「\1」',
        raw,
    )
    replacements = {
        '으로옳은': '으로 옳은',
        '으로옳지': '으로 옳지',
        '에의해': '에 의해',
        '에의하여': '에 의하여',
        '할때': '할 때',
        '있을때': '있을 때',
        '외에는그': '외에는 그',
        '약할때': '약할 때',
    }
    for before, after in replacements.items():
        raw = raw.replace(before, after)
    return raw


def has_suspicious_text_artifact(value: str) -> bool:
    """Detect high-confidence text order artifacts that should not be exported."""
    text = str(value or '').strip()
    if not text:
        return False
    for match in re.finditer(r'[가-힣]{1,8}\d+(?:,\d{3})*(?:\.\d+)?\s+[가-힣]{1,8}', text):
        if re.fullmatch(r'기관[1-3]\s+문제', match.group(0)):
            continue
        context = text[max(0, match.start() - 12):match.end() + 12]
        if re.search(r'제?\d+조(?:의\d+)?\s*제?\d+항', context):
            continue
        return True
    suspect_patterns = [
        r'(?:→\s*){2,}$',
        r'제\s+번\s+\d+\s+',
        r'제\s+심\s+\d+\s+',
        r'행정\s+사이클.+?\s+[24]\s+(?:있|하중|인장)',
        r'상\s+유도전동기.+?\s+3\s+회전자계',
        r'전압\s+증폭률이\s+배인.+?\s+100\s+나타내면',
        r'총톤수\s+톤.+?\s+\d+\s+',
        r'길이\s+미터.+?\s+\d+\s+',
        r'해리\s+(?:이상|이하)\s+\d+\s+',
        r'개의\s+\d+\s+(?:물표|마스트등|원뿔)',
        r'분을\s+\d+\s+넘지',
        r'장음\s+회와\s+단음\s+회\s+신호를\s+\d+\s+\d+',
        r'몇\s+\d+\s+(?:회|헥토파스칼)',
        r'시간을\s+\d+\s+초과',
        r'년이\s+\d+\s+경과',
        r'탑승한\s+\d+\s+모든',
        r'선원이\s+\d+\s+실행',
        r'^(?:약\s+)?(?:이상|이하|미만|초과)\s*\d',
        r'^(?:미터|센티미터)\s+(?:이상|이하|미만|초과)\s*\d',
        r'(?:\]\d{1,2}\.|[가-힣]\d{1,2}\.)[0-9A-Za-z가-힣\[].+\?',
        r'협약.+?\bSOLAS\b\s+(?:사용|옳|것은)',
        r'\bISM code\s+식별',
        r'선박용\s+수신기.+?\bGPS\s+아닌',
        r'경우에\d+(?:\.\d+)?서도',
    ]
    return any(re.search(pattern, text) for pattern in suspect_patterns)


