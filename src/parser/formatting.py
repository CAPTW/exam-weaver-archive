def _repair_quoted_term_prompt_order(text: str) -> str:
    original = str(text or '')
    raw = original.strip().replace('“', '"').replace('”', '"')

    match = re.match(r'^의\s+뜻은"(?P<term>[^"]+)"\?$', raw)
    if match:
        return f'"{match.group("term")}"의 뜻은?'

    match = re.match(r'^란"(?P<term>[^"]+)"\?$', raw)
    if match:
        return f'"{match.group("term")}"란?'

    match = re.match(r'^다음\s+중\s+와\s+가장\s+관련이\s+있는\s+기관은"(?P<term>[^"]+)"\?$', raw)
    if match:
        return f'다음 중 "{match.group("term")}"와 가장 관련이 있는 기관은?'

    match = re.match(r'^가\s+뜻하는\s+것은"(?P<term>[^"]+)"\?$', raw)
    if match:
        return f'"{match.group("term")}"가 뜻하는 것은?'

    match = re.match(r'^가\s+뜻하는\s+것"(?P<term>[^"]+)"\s+은\?$', raw)
    if match:
        return f'"{match.group("term")}"가 뜻하는 것은?'

    match = re.match(r'^가\s+아닌\s+것은"(?P<term>[^"]+)"\?$', raw)
    if match:
        return f'"{match.group("term")}"가 아닌 것은?'

    match = re.match(
        r'^에서\s+"(?P<sentence>[^"]+)"\s+에\s+속하는\s+것은"(?P<term>[^"]+)"\?$',
        raw,
    )
    if match:
        return f'"{match.group("sentence")}"에서 "{match.group("term")}"에 속하는 것은?'

    if raw.startswith('다음 중 이면서 동시"One revolution of crank shaft"'):
        return (
            '다음 중 "One revolution of crank shaft"이면서 동시에 '
            '"Half revolution of cam shaft"인 기관은 어느 것인가?'
        )

    return original


def _repair_english_tail_particle_prompt(text: str) -> str:
    raw = str(text or '')
    match = re.match(
        (
            r'^(?P<particle>에서|에게|으로|부터|까지|[은는이가을를의에와과로])\s*'
            r'(?P<body>.+?)'
            r'(?P<term>[A-Z][A-Za-z0-9]*(?:[-/ ][A-Za-z0-9]+)*(?:\s*\([^?()]+\))?)\?$'
        ),
        raw,
    )
    if not match:
        return raw

    body = match.group('body').strip()
    term = match.group('term').strip()
    if not body or not term:
        return raw
    return f"{term}{match.group('particle')} {body}?"


def _repair_prompt_tail_inside_quoted_sentence(text: str) -> str:
    original = str(text or '')
    raw = original.replace('“', '"').replace('”', '"')
    prompt = r'(?P<prompt>알맞은|적절하지 않은)'

    def rebuild(match):
        before = match.group('before').strip()
        after = match.group('after').strip()
        blank = match.group('blank')
        prompt_text = match.group('prompt')
        return f'"{before} {after}"에서 {blank}에 {prompt_text} 것은?'

