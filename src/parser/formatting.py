def apply_overline_marks(text: str, overline_marks: List) -> str:
    """Insert LaTeX overline commands using positioned PDF line marks."""
    raw = str(text or '')
    replacements = []
    occupied_until = 0

    for mark in overline_marks or []:
        line_text, start, end, marked_text = _overline_mark_parts(mark)
        if not marked_text:
            continue

        match = _find_positioned_mark(raw, line_text, start, end, occupied_until)
        if match is None and len(marked_text.strip()) >= 2:
            match = _find_next_text_match(raw, marked_text, occupied_until)
        if match is None:
            continue

        target_start, target_end = match
        if target_start < occupied_until:
            continue
        inner = _normalize_formula_inner(raw[target_start:target_end])
        latex = rf'\overline{{{inner}}}'
        replacements.append((target_start, target_end, latex))
        occupied_until = target_end

    if not replacements:
        return raw

    output = []
    position = 0
    for start, end, replacement in replacements:
        output.append(raw[position:start])
        output.append(replacement)
        position = end
    output.append(raw[position:])
    return ''.join(output)


def merge_spans(*span_groups: List[dict]) -> List[dict]:
    """Merge span groups, combining attributes for identical ranges."""
    by_range = {}
    for spans in span_groups:
        for span in spans or []:
            try:
                key = (int(span.get('start')), int(span.get('end')))
            except (TypeError, ValueError, AttributeError):
                continue
            if key[1] <= key[0]:
                continue
            by_range.setdefault(key, {'start': key[0], 'end': key[1]}).update(
                {k: v for k, v in span.items() if k not in {'start', 'end'}}
            )
    return [
        by_range[key]
        for key in sorted(by_range)
    ]


def existing_latex_spans(format_json, text: str) -> List[dict]:
    """Return valid existing LaTeX spans for unchanged text."""
    import json

    if not format_json:
        return []
    try:
        payload = json.loads(format_json) if isinstance(format_json, str) else format_json
    except (TypeError, ValueError):
        return []
    spans = payload.get('spans') if isinstance(payload, dict) else []
    valid = []
    for span in spans or []:
        latex = span.get('latex') if isinstance(span, dict) else None
        if not latex:
            continue
        try:
            start = int(span.get('start'))
            end = int(span.get('end'))
        except (TypeError, ValueError):
            continue
        if 0 <= start < end <= len(text) and text[start:end] == latex:
            valid.append({'start': start, 'end': end, 'latex': latex})
    return valid


def _normalize_latex(value: str) -> str:
    latex = str(value or '').strip()
    if not latex:
        return ''
    if latex.startswith('sqrt'):
        latex = '\\' + latex
    if latex.startswith('overline'):
        latex = '\\' + latex
    return latex


def _normalize_formula_inner(value: str) -> str:
    inner = str(value or '').strip()
    inner = re.sub(r'\s*[·ㆍ∙•]\s*', r' \\cdot ', inner)
    inner = re.sub(r'\s*([+=\-])\s*', r'\1', inner)
    inner = re.sub(r'\s+', ' ', inner)
    return inner.strip()


def _looks_like_inline_formula(value: str) -> bool:
    text = str(value or '')
    return bool(
        re.search(r'√|\\sqrt|×|\\times|θ|\\theta|(?<![A-Za-z])(?:cos|sin|tan)\s*[A-Za-z\\θ]', text)
    )


def _normalize_formula_expression(value: str) -> str:
    expr = str(value or '').strip()
    expr = re.sub(
        r'(?<!\\)sqrt\s*\{\s*([^{}]+?)\s*\}',
        lambda match: rf'\sqrt{{{_normalize_formula_inner(match.group(1))}}}',
        expr,
        flags=re.IGNORECASE,
    )
    expr = re.sub(
        r'√\s*\{\s*([^{}]+?)\s*\}',
        lambda match: rf'\sqrt{{{_normalize_formula_inner(match.group(1))}}}',
        expr,
    )
    expr = re.sub(
        r'√\s*([0-9])(?=[0-9A-Za-z\\θ])',
        lambda match: rf'\sqrt{{{_normalize_formula_inner(match.group(1))}}}',
        expr,
    )
    expr = re.sub(
        r'√\s*([0-9]+(?:\.[0-9]+)?)',
        lambda match: rf'\sqrt{{{_normalize_formula_inner(match.group(1))}}}',
        expr,
    )
    expr = re.sub(
        r'√\s*([A-Za-z][A-Za-z0-9_]*)',
        lambda match: rf'\sqrt{{{_normalize_formula_inner(match.group(1))}}}',
        expr,
    )
    expr = expr.replace('Θ', r'\theta').replace('θ', r'\theta')
    expr = re.sub(r'\s*[×]\s*', r' \\times ', expr)
    expr = re.sub(r'\s*[·ㆍ∙•]\s*', r' \\cdot ', expr)
    expr = re.sub(
        r'(?<!\\)\b(cos|sin|tan)\s*(?=\\theta|[A-Za-z])',
        lambda match: '\\' + match.group(1),
        expr,
    )
    expr = re.sub(r'\\(cos|sin|tan)\s+(\\theta)', r'\\\1\2', expr)
    expr = re.sub(r'\s*=\s*', '=', expr)
    expr = re.sub(r'\s*\\times\s*', r' \\times ', expr)
    expr = re.sub(r'\s*\\cdot\s*', r' \\cdot ', expr)
    expr = re.sub(r'\s+', ' ', expr)
    return expr.strip()


def _overline_mark_parts(mark):
    if isinstance(mark, dict):
        return (
            str(mark.get('line_text') or ''),
            _safe_int(mark.get('start')),
            _safe_int(mark.get('end')),
            str(mark.get('text') or ''),
        )
    line_text = getattr(mark, 'line_text', '')
    start = getattr(mark, 'start', None)
    end = getattr(mark, 'end', None)
    text = getattr(mark, 'text', None)
    if text is not None or line_text:
        return str(line_text or ''), _safe_int(start), _safe_int(end), str(text or '')
    return '', None, None, str(mark or '')


def _safe_int(value):
    try:
        return int(value)
    except (TypeError, ValueError):
        return None


def _find_positioned_mark(raw: str, line_text: str, start, end, search_from: int):
    if not line_text or start is None or end is None or end <= start:
        return None
    line_offset = raw.find(line_text, search_from)
    if line_offset < 0:
        return None
    target_start = line_offset + start
    target_end = line_offset + end
    if 0 <= target_start < target_end <= len(raw):
        return target_start, target_end
    return None


def _find_next_text_match(raw: str, needle: str, search_from: int):
    needle = str(needle or '').strip()
    if not needle:
        return None
    found = raw.find(needle, search_from)
    if found >= 0:
        return found, found + len(needle)

    compact = re.sub(r'\s+', '', needle)
    if not compact:
        return None
    for start in range(search_from, len(raw)):
        position = start
        matched = 0
        first = None
        while position < len(raw) and matched < len(compact):
            if raw[position].isspace():
                position += 1
                continue
            if raw[position] != compact[matched]:
                break
            if first is None:
                first = position
            matched += 1
            position += 1
        if matched == len(compact) and first is not None:
            return first, position
    return None
